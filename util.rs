static mut log_mask: libc::c_int = 0;
#[no_mangle]
pub static mut logout: *mut FILE = 0 as *const FILE as *mut FILE;
#[no_mangle]
pub unsafe extern "C" fn logg(mut lvl: libc::c_short,
                              mut fmt: *const libc::c_char, mut args: ...) {
    let mut t: time_t = 0;
    let mut args_0: ::std::ffi::VaListImpl;
    let mut tm: *mut tm = 0 as *mut tm;
    if lvl as libc::c_int & log_mask == 0 { return }
    if logout.is_null() { logout = stdout }
    args_0 = args.clone();
    time(&mut t);
    tm = localtime(&mut t);
    fprintf(logout,
            b"%02d-%02d-%02d %02d:%02d:%02d \x00" as *const u8 as
                *const libc::c_char, (*tm).tm_year % 100 as libc::c_int,
            (*tm).tm_mon + 1 as libc::c_int, (*tm).tm_mday, (*tm).tm_hour,
            (*tm).tm_min, (*tm).tm_sec);
    vfprintf(logout, fmt, args_0.as_va_list());
    fflush(logout);
}
#[no_mangle]
pub unsafe extern "C" fn logg_setmask(mut mask: libc::c_int) {
    log_mask = mask;
}
